export function localizeDistrictName(name: string | null | undefined, isHi: boolean): string {
  const value = (name ?? "").trim();
  if (!isHi || !value) return value;
  return DISTRICT_HI[value.toLowerCase()] ?? value;
}

export function localizeCategoryName(name: string | null | undefined, isHi: boolean): string {
  const value = (name ?? "").trim();
  if (!isHi || !value) return value;
  return CATEGORY_HI[value.toLowerCase()] ?? value;
}
